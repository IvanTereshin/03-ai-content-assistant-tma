import { scenarios, toneLabels } from './data';
import type { AiCommand, BrandSettings, Generation, GeneratorForm, ScenarioId } from './types';

const actionMap: Record<ScenarioId, string> = {
  ideas: 'Идеи постов',
  post: 'Пост',
  rewrite: 'Рерайт',
  plan: 'План на неделю',
  hooks: 'Hooks / CTA',
};

const byScenario = (id: ScenarioId) => scenarios.find((scenario) => scenario.id === id)?.title ?? actionMap[id];

export function createGeneration(
  scenarioId: ScenarioId,
  form: GeneratorForm,
  brand: BrandSettings,
): Generation {
  return {
    id: crypto.randomUUID(),
    scenarioId,
    scenarioTitle: byScenario(scenarioId),
    topic: form.topic,
    createdAt: new Date().toISOString(),
    saved: false,
    variants: buildVariants(scenarioId, form, brand),
  };
}

function buildVariants(scenarioId: ScenarioId, form: GeneratorForm, brand: BrandSettings): string[] {
  const tone = toneLabels[form.tone].toLowerCase();

  if (scenarioId === 'ideas') {
    return [
      `1. Почему ${form.topic.toLowerCase()} — это не вопрос вдохновения, а вопрос процесса.\n2. 5 признаков, что контент-план уже устарел.\n3. Разбор: как один тезис превратить в серию из 4 постов.\n4. Чеклист перед публикацией: польза, позиция, следующий шаг.\n5. Личный пост: где автор теряет регулярность и как это исправить.\n\nФокус для аудитории: ${form.audience}. Тон: ${tone}.`,
      `Серия на неделю:\nПн — боль: автор пишет рывками.\nВт — причина: нет банка идей и форматов.\nСр — решение: шаблон темы, хука и CTA.\nЧт — пример: один черновик в трех форматах.\nПт — продажа: консультация по контент-системе.\n\nЦель серии: ${form.goal}.`,
      `Углы подачи:\n- Практичный: "Как собрать 20 тем за 30 минут".\n- Экспертный: "Почему AI без tone of voice портит доверие".\n- Личный: "Что я перестал делать, чтобы писать стабильнее".\n- Провокационный: "Контент-план не спасает, если нет редакторской позиции".`,
    ];
  }

  if (scenarioId === 'rewrite') {
    const source = form.sourceText || form.topic;
    return [
      `Было ощущение хаоса, стало понятно: ${source}\n\nСильная версия:\nАвторы часто пропадают не потому, что им нечего сказать. Проблема в отсутствии системы. Нужны три простые вещи: банк тем, слот для черновиков и короткая проверка перед публикацией. Тогда каждый пост начинается не с паники, а с заготовки.`,
      `Коротко и яснее:\nРегулярность канала держится не на мотивации. Она держится на процессе: идеи заранее, черновики по расписанию, финальная проверка пользы и CTA. Так автор пишет стабильнее и не выгорает.`,
      `Экспертнее:\nГлавная ошибка автора — путать производство контента с поиском вдохновения. Надежнее разделить процесс на этапы: накопление идей, создание черновиков, редактура, публикация и анализ реакции. AI полезен именно между идеей и первой версией текста.`,
    ];
  }

  if (scenarioId === 'plan') {
    return [
      `План на 7 дней для ниши "${brand.niche}":\nПн: проблема недели — почему авторы теряют регулярность.\nВт: обучающий пост — 3 уровня контент-системы.\nСр: кейс — как из одного тезиса сделать 5 материалов.\nЧт: личный опыт — что изменилось после внедрения AI-редактора.\nПт: продающий пост — аудит контент-процесса.\nСб: вовлекающий вопрос — где вы чаще застреваете?\nВс: итоги — что сохранить и применить на следующей неделе.`,
      `Контент-ритм:\n1. Понедельник — боль и узнавание.\n2. Вторник — понятная методика.\n3. Среда — разбор примера.\n4. Четверг — мнение автора.\n5. Пятница — оффер.\n6. Суббота — диалог.\n7. Воскресенье — сбор обратной связи.\n\nТак неделя не выглядит как набор случайных постов.`,
      `Мини-календарь с CTA:\nПн: сохранить чеклист.\nВт: написать "система" в личку.\nСр: выбрать лучший хук.\nЧт: ответить на опрос.\nПт: записаться на консультацию.\nСб: поделиться своей проблемой.\nВс: забрать шаблон планирования.`,
    ];
  }

  if (scenarioId === 'hooks') {
    return [
      `Hooks:\n- Если пост начинается с "сегодня поговорим", вы уже потеряли часть читателей.\n- Контент-план не решает проблему, если внутри нет позиции автора.\n- Самый дорогой этап поста — не написание, а старт с пустого листа.\n\nCTA:\nСохраните пост и проверьте свой следующий черновик по этим пунктам.`,
      `Зацепки для ${form.audience}:\n1. "Вы не ленитесь писать. У вас просто нет редакционного процесса."\n2. "AI ускоряет автора только там, где уже понятна мысль."\n3. "Стабильность канала начинается до открытия редактора."\n\nМягкий CTA: Напишите мне слово "контент", если хотите разобрать свою систему публикаций.`,
      `Продающие CTA:\n- Хотите такую систему под свой канал? Напишите "аудит".\n- Сохраните и примените к ближайшему посту сегодня.\n- Пришлите черновик в личку, покажу, где теряется смысл.`,
    ];
  }

  return [
    `Хук: Автору не нужен еще один список идей. Ему нужна система, которая доводит идею до публикации.\n\n${form.topic}\n\nДля ${form.audience} проблема обычно не в отсутствии мыслей. Мысли есть, но они разбросаны по заметкам, чатам и голове. Поэтому каждый новый пост начинается с тяжелого вопроса: "о чем писать?"\n\nРабочий процесс проще:\n1. Собрать банк тем по болям аудитории.\n2. Выбрать цель поста: сохранение, диалог или заявка.\n3. Написать первый черновик без попытки сделать идеально.\n4. Проверить текст: есть ли польза, позиция и следующий шаг.\n\nAI здесь полезен как редактор скорости. Он предлагает варианты, но финальное решение остается за автором.\n\nCTA: Сохраните этот алгоритм и примените к ближайшему посту.`,
    `Короткая версия:\n${form.topic}\n\nРегулярный канал строится не на вдохновении. Он строится на понятной цепочке: тема -> черновик -> редактура -> публикация -> анализ.\n\nЕсли автор держит все в голове, он быстро устает. Если есть банк идей, тон бренда и шаблон проверки, писать становится легче.\n\nМини-чеклист перед публикацией:\n- читатель узнает свою проблему;\n- есть конкретная мысль автора;\n- текст можно сохранить или переслать;\n- понятен следующий шаг.\n\nCTA: Проверьте по этому списку последний пост в канале.`,
    `Более продающий вариант:\nБольшинство каналов теряют заявки не из-за плохих идей. Они теряют их из-за нерегулярности и слабых CTA.\n\nКогда у автора есть система, каждый пост работает на одну цель: объяснить ценность, укрепить доверие или привести к диалогу.\n\nЧто нужно настроить:\n1. Ниша и аудитория без общих слов.\n2. Tone of voice, чтобы AI не писал "как все".\n3. Банк тем под боли читателей.\n4. Контент-план на неделю с понятными статусами.\n\nCTA: Если хотите собрать такую систему для своего канала, напишите "план" в личку.`,
  ];
}

export function transformDraft(command: AiCommand, text: string): string {
  const cleanText = text.trim();

  if (!cleanText) {
    return 'Добавьте черновик, и команда применит к нему стиль.';
  }

  if (command === 'shorter') {
    return `${cleanText.split('. ').slice(0, 3).join('. ')}.\n\nИтог: меньше воды, больше смысла и один понятный следующий шаг.`;
  }

  if (command === 'simpler') {
    return cleanText
      .replaceAll('регулярность', 'стабильный выпуск')
      .replaceAll('редактура', 'правка')
      .replaceAll('аудитория', 'читатели')
      .concat('\n\nПроще: сначала мысль, потом пример, потом действие для читателя.');
  }

  if (command === 'expert') {
    return `Экспертная версия:\n\n${cleanText}\n\nДобавьте рамку принятия решения: какая проблема решается, почему старый подход не работает, какой процесс нужен и как понять, что он дал результат. Так текст звучит не как совет из списка, а как позиция специалиста.`;
  }

  return `Продающая версия:\n\n${cleanText}\n\nПереход к офферу: если вы хотите не просто писать чаще, а выстроить понятную систему публикаций под свой канал, начните с аудита текущих тем, CTA и tone of voice. Напишите "аудит" — покажу, где сейчас теряется конверсия.`;
}
